package crm_app.config;

public class Roles {
	private String idRoles;
	private String nameRoles;
	private String descriptionRoles;
	
	
	
	public String getIdRoles() {
		return idRoles;
	}
	public void setIdRoles(String idRoles) {
		this.idRoles = idRoles;
	}
	public String getNameRoles() {
		return nameRoles;
	}
	public void setNameRoles(String nameRoles) {
		this.nameRoles = nameRoles;
	}
	public String getDescriptionRoles() {
		return descriptionRoles;
	}
	public void setDescriptionRoles(String descriptionRoles) {
		this.descriptionRoles = descriptionRoles;
	}
	
	

}
