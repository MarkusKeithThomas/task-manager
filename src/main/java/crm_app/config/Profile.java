package crm_app.config;

public class Profile {
	private String idTask;
	private String nameTask;
	private String nameJob;
	private String dateStatedJob;
	private String dateFinishedJob;
	private String statusJob;
	
	
	public String getIdTask() {
		return idTask;
	}
	public void setIdTask(String idTask) {
		this.idTask = idTask;
	}
	public String getNameTask() {
		return nameTask;
	}
	public void setNameTask(String nameTask) {
		this.nameTask = nameTask;
	}
	public String getNameJob() {
		return nameJob;
	}
	public void setNameJob(String nameJob) {
		this.nameJob = nameJob;
	}
	public String getDateStatedJob() {
		return dateStatedJob;
	}
	public void setDateStatedJob(String dateStatedJob) {
		this.dateStatedJob = dateStatedJob;
	}
	public String getDateFinishedJob() {
		return dateFinishedJob;
	}
	public void setDateFinishedJob(String dateFinishedJob) {
		this.dateFinishedJob = dateFinishedJob;
	}
	public String getStatusJob() {
		return statusJob;
	}
	public void setStatusJob(String statusJob) {
		this.statusJob = statusJob;
	}
	
	

}
