package crm_app.config;

public class Status {
	private String idStatus;
	private String nameStatuString;
	private String countStatus;
	
	
	
	public String getCountStatus() {
		return countStatus;
	}
	public void setCountStatus(String countStatus) {
		this.countStatus = countStatus;
	}
	public String getIdStatus() {
		return idStatus;
	}
	public void setIdStatus(String idStatus) {
		this.idStatus = idStatus;
	}
	public String getNameStatuString() {
		return nameStatuString;
	}
	public void setNameStatuString(String nameStatuString) {
		this.nameStatuString = nameStatuString;
	}
	
	

}
